String reArrangeText(String str) {
  try {
    var barrier = 0;
    for (var i = 0; i < str.length; i++) {
      if (i < str.length && isBanglaPreKar(str[i])) {
        var j = 1;
        while (isBanglaBanjonborno(str[i - j])) {
          if (i - j < 0) {
            break;
          }
          if (i - j <= barrier) {
            break;
          }
          if (isBanglaHalant(str[i - j - 1])) {
            j += 2;
          } else {
            break;
          }
        }
        var temp = str.substring(0, i - j);
        temp += str[i];
        temp += str.substring(i - j, i);
        temp += str.substring(i + 1, str.length);
        str = temp;
        barrier = i + 1;
        continue;
      }
      if (i < str.length - 1 &&
          isBanglaHalant(str[i]) &&
          str[i - 1] == ansiCodeList[257] &&
          !isBanglaHalant(str[i - 2])) {
        var j = 1;
        var foundPreKar = 0;
        while (true) {
          if (isBanglaBanjonborno(str[i + j]) && isBanglaHalant(str[i + j + 1])) {
            j += 2;
          } else {
            if (isBanglaBanjonborno(str[i + j]) && isBanglaPreKar(str[i + j + 1])) {
              foundPreKar = 1;
              break;
            } else {
              break;
            }
          }
        }
        var temp = str.substring(0, i - 1);
        temp += str.substring(i + j + 1, i + j + foundPreKar + 1);
        temp += str.substring(i + 1, i + j + 1);
        temp += str[i - 1];
        temp += str[i];
        temp += str.substring(i + j + foundPreKar + 1, str.length);
        str = temp;
        i += (j + foundPreKar);
        barrier = i + 1;
        continue;
      }
    }
    return str;
  } catch (_) {
    return str;
  }
}

